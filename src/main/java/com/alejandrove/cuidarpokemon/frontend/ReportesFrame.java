    private void tipoLogComboBoxActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_tipoLogComboBoxActionPerformed
        int tipoElegido = tipoLogComboBox.getSelectedIndex();
        menorMayorButton.setVisible(false);
        mayorMenorButton.setVisible(false);
        switch (tipoElegido) {
            case 0:
                rellenarLogsFecha();
                break;
            case 1:
                rellenarTipo(LogTypes.enfermedad);
                break;
            case 2:
                rellenarTipo(LogTypes.comida);
                break;
            case 3:
                rellenarTipo(LogTypes.paseo);
                break;
            case 4:
                rellenarTipo(LogTypes.nivel);
                menorMayorButton.setVisible(true);
                mayorMenorButton.setVisible(true);
                break;
            case 5:
                rellenarTipo(LogTypes.muerte);
                break;
            case 6:
                rellenarTipo(LogTypes.nacimiento);
                break;
            case 7:
                rellenarTipo(LogTypes.limpieza);
                break;
            default:
                break;
        }
    }// GEN-LAST:event_tipoLogComboBoxActionPerformed


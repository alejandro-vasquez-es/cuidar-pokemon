    public void rellenarLogsFecha() {
        realLogs = new Log[HelperClass.getTotalLogs(logs)];
        logsString = new String[realLogs.length];
        for (int i = 0; i < realLogs.length; i++) {
            realLogs[i] = logs[i];
            logsString[i] = realLogs[i].toString();
        }
        logsList.setListData(logsString);
    }

    public void rellenarTipo(String tipo) {
        String[] logsTipoString = Arrays
                .stream(Arrays.stream(realLogs).filter(log -> log.getTipo() == tipo).toArray(Log[]::new))
                .map(log -> log.toString()).toArray(String[]::new);
        logsList.setListData(logsTipoString);

    }


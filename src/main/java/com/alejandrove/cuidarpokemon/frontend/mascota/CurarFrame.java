    public void rellenarMedicinasJugador() {
        String[] medicinasJugadorString = new String[juego.jugador.getTotalMedicinas()];
        medicinasJugador = new Medicina[juego.jugador.getTotalMedicinas()];
        for (int i = 0; i < medicinasJugadorString.length; i++) {
            medicinasJugador[i] = juego.jugador.medicinas[i];
            medicinasJugadorString[i] = juego.jugador.medicinas[i].getNombre();
        }
        medicinasJugadorList.setListData(medicinasJugadorString);
    }


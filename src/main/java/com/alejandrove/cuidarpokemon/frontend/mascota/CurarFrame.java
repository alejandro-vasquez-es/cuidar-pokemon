    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        if (mascota.getPeticionesCurar() > 0) {
            try {
                if (mascota.getEstado() != EstadosTypes.muerto) {

                    int indiceMedicinaSeleccionada = medicinasJugadorList.getLeadSelectionIndex();
                    Medicina medicinaAUsar = medicinasJugador[indiceMedicinaSeleccionada];
                    mascota.curar(medicinaAUsar);
                    juego.jugador.medicinas = Arrays.stream(juego.jugador.medicinas)
                            .filter(medicina -> !medicinaAUsar.equals(medicina))
                            .toArray(Medicina[]::new);
                    juego.jugador.disminuirMedicinas();
                    rellenarMedicinasJugador();
                    JOptionPane.showMessageDialog(this,
                            "La medicina " + medicinaAUsar.getNombre() + " se le dio al pokemon",
                            "Pokemon curar", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    // TODO: Manejar esto como excepción
                    JOptionPane.showMessageDialog(this,
                            "No puedes realizar esta acción porque tu pokemon esta muerto :(, puedes revivirlo desde su ventana",
                            "Error al curar", JOptionPane.ERROR_MESSAGE);
                }
            } catch (ArrayIndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(this,
                        "No has seleccionado ninguna medicina, puedes comprar en la tienda",
                        "Error al curar", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Tu mascota no necesita ser curada",
                    "Curar mascota", JOptionPane.INFORMATION_MESSAGE);
        }

    }// GEN-LAST:event_jButton1ActionPerformed


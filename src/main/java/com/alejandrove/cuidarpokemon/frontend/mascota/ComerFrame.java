    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // alimenta a la mascota y le elimina la comida al jugador
        try {
            if (mascota.getEstado() != EstadosTypes.muerto) {

                int indiceComidaSeleccionada = comidasJugadorList.getLeadSelectionIndex();
                Comida comidaAUsar = comidasJugador[indiceComidaSeleccionada];
                mascota.comer(comidaAUsar);
                juego.jugador.comidas = Arrays.stream(juego.jugador.comidas)
                        .filter(comida -> !comidaAUsar.equals(comida))
                        .toArray(Comida[]::new);
                juego.jugador.disminuirComidas();
                rellenarComidasJugador();
                JOptionPane.showMessageDialog(this, "La comida " + comidaAUsar.getNombre() + " se le dio al pokemon",
                        "Pokemon alimentado", JOptionPane.INFORMATION_MESSAGE);

            } else {
                // TODO: Manejar esto como excepción
                JOptionPane.showMessageDialog(this,
                        "No puedes realizar esta acción porque tu pokemon esta muerto :(, puedes revivirlo desde su ventana",
                        "Error al comer", JOptionPane.ERROR_MESSAGE);
            }
        } catch (ArrayIndexOutOfBoundsException e) {
            JOptionPane.showMessageDialog(this,
                    "No has seleccionado ninguna comida, puedes comprar en la tienda",
                    "Error al comer", JOptionPane.ERROR_MESSAGE);
        }

    }// GEN-LAST:event_jButton1ActionPerformed


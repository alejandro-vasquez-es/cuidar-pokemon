    public void rellenarComidasJugador() {
        String[] comidasJugadorString = new String[juego.jugador.getTotalComidas()];
        comidasJugador = new Comida[juego.jugador.getTotalComidas()];
        for (int i = 0; i < comidasJugadorString.length; i++) {
            comidasJugador[i] = juego.jugador.comidas[i];
            comidasJugadorString[i] = juego.jugador.comidas[i].getNombre();
        }
        comidasJugadorList.setListData(comidasJugadorString);
    }

